#[derive(Debug)]
pub struct Token {
    pub line: usize,
    pub kind: TokenKind,
}
#[derive(PartialEq, Eq, Debug)]
pub enum TokenKind {
    LeftBrace,
    RightBrace,
    Equal,
    Coma,
    Plus,
    Minus,
    Slash,
    Star,
    Caret,
    LeftParen,
    RightParen,
    LeftBracket,
    RightBracket,
    Number(u64),
    Semi,
    Colon,
    Fun,
    Print,
    Borrow,
    Ident(String),
    Int,
    Bool,
    String,
    StringLiteral(String),
    Ref,
    Box,
    List,
    Static,
    Let,
    Mut,
    Imm,
    Some,
    Case,
    In,
    Option,
    None,
    Panic,
    For,
    Arrow,
    Error,
}
